package conditionStatement;

public class IfElseIFCondition {
    public static void main(String[] args) {
        System.out.println("");
        int num = 45;
        if (num > 90) {
            System.out.println("Grade A");
        } else if (num > 70) {
            System.out.println("Grade B");

        } else if (num > 50) {
            System.out.println("Grade C");

        } else {
            System.out.println("Fail");

        }
    }
}
