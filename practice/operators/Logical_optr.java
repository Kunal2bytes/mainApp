package practice.operators;

public class Logical_optr {
    public static void main(String[] args) {
       boolean a=true, b=true,c=false;
        System.out.println(a&&b); // && return true when both value true
        System.out.println(a||c); // || return true when one value true
        System.out.println(!a); // || return opposite

    }
}
