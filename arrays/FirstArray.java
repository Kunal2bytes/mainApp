package arrays;

public class FirstArray {
    public static void main(String[] args) {
        int [] a= {101,102,103,104};
        System.out.println(a.length);//4
        System.out.println(a[3]);//104
        for (int i=0; i<a.length;i++){
            System.out.println(a[i]);
        }
    }
}
